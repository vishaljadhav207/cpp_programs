#include <iostream>
#include <math.h>
using namespace std;

int main()
{
    cout << "sin (100)=" << sin(100.00) << endl;
    cout << "cos (100)=" << cos(100.00) << endl;
    cout << "tan (100)=" << tan(100.00) << endl;
    cout << "7 to the 6=" << pow(7.0, 6.0) << endl;
    cout << "log10(10)=" << log10(10.00) << endl;
    cout << "sqrt(2)=" << sqrt(2.00) << endl;
    return 0;
}